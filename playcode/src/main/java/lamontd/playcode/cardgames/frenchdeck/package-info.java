/**
 * Package that encapsulates the functionality of a French card deck.
 * <p>
 * The suits that are normally represented in cards -- spades, hearts,
 * diamonds, and clubs -- originated in France in around 1480. The 
 * ranks of the court cards and the people that they represent are 
 * from the French as well:
 * <ul>
 * <li><b>King of Spades</b>: King David</li>
 * <li><b>King of Hearts</b>: Charlemagne (King Charles I)</li>
 * <li><b>King of Diamonds</b>: Julius Caesar</li>
 * <li><b>King of Clubs</b>: Alexander the Great</li>
 * <li><b>Queen of Spades</b>: Pallas Athena</li>
 * <li><b>Queen of Hearts</b>: Judith</li>
 * <li><b>Queen of Diamonds</b>: Rachel (either Biblical or the pseudonym of Charlemagne's mistress)</li>
 * <li><b>Queen of Clubs</b>: Argine</li>
 * <li><b>Jack of Spades</b>: Ogier the Dane (a knight of Charlemagne)</li>
 * <li><b>Jack of Hearts</b>: La Hire (comrade-in-arms to Joan of Arc)</li>
 * <li><b>Jack of Diamonds</b>: Hector</li>
 * <li><b>Jack of Clubs</b>: Judas Maccabeus or Lancelot</li>
 * </ul>
 * <p>
 * This is the deck that is used for most of the standard games of cards that people are used to.
 *
 */
